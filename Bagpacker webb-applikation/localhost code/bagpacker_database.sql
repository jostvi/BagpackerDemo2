drop table if exists all_items;
drop table if exists temperature;
drop table if exists transport;
drop table if exists accommodation;
drop table if exists activities;
drop table if exists climate_classification
drop table if exists saved_lists;
drop table if exists weather_data;



CREATE TABLE all_items (
id SERIAL PRIMARY key, 
item varchar(255) NOT NULL, 
category varchar(255), 
weight FLOAT NOT NULL, 
hand_luggage INT NOT NULL, 
general BOOLEAN NOT NULL, 
quantity FLOAT NOT NULL, 
min_length INT);


create table temperature (
item_id INT PRIMARY key REFERENCES all_items(id),
temp_min INT, 
temp_max INT, 
other_dependencies boolean, 
rain boolean);


create table transport (
item_id INT REFERENCES all_items(id),
type varchar(255), 
PRIMARY key(item_id, type));


create table accommodation (
item_id INT REFERENCES all_items(id),
type varchar(255),
PRIMARY key(item_id, type));


create table activities (
item_id INT REFERENCES all_items(id),
activity varchar(255), 
PRIMARY key(item_id, activity));


create table climate_classification (
zone varchar(255) PRIMARY KEY, 
min_temp_winter INT, 
max_temp_winter INT, 
mean_winter INT, 
rain_winter boolean, 
min_temp_summer INT, 
max_temp_summer INT, 
mean_summer INT, 
rain_summer boolean, 
min_temp_spring INT, 
max_temp_spring INT, 
mean_spring INT, 
rain_spring boolean, 
min_temp_autumn INT, 
max_temp_autumn INT, 
mean_autumn INT, 
rain_autumn boolean);

CREATE TABLE saved_lists (
code INT, 
item_id INT, 
quantity INT, 
checked BOOLEAN, 
PRIMARY KEY (code, item_id));


CREATE TABLE weather_data (
id INT PRIMARY KEY, 
destination varchar(100), 
start varchar(100), finish varchar(100), 
temp_min INT, 
temp_max INT, 
temp_mean INT, 
which_data varchar(100), 
rain BOOLEAN);



